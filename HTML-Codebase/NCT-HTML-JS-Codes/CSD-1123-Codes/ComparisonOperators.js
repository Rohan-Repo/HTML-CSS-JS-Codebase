let x = 10;
let y = 20;

console.log( 'Equal to (==):');
console.log( '\t x == 10 : ', x == 10 );
console.log( '\t y == 1 : ', y == 1 );

console.log( 'Equal Value & Type (===):');
console.log( '\t x === 10 : ', x === 10 );
console.log( '\t x === "10" : ', x === "10" );

console.log( 'Not Equal to (!=):');
console.log( '\t x != 10 : ', x != 10 );
console.log( '\t y != 1 : ', y != 1 );

console.log( 'Not Equal to & Type (!==):');
console.log( '\t x !== 10 : ', x !== 10 );
console.log( '\t x !== "10" : ', x !== "10" );

console.log( 'x: ', x );
console.log( 'y: ', y );

console.log( 'Less than & Less Than Equal to:');
console.log( '\t x <= 10 : ', x <= 10 );
console.log( '\t y < 10 : ', y < 10 );

console.log( 'Greater than & Greater Than Equal to:');
console.log( '\t x >= 10 : ', x >= 10 );
console.log( '\t y > 10 : ', y > 10 );